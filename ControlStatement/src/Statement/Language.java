package Statement;

public class Language {

	public static void main(String[] args) {
		String lang = "Java";
		if(lang == "Python") {
			System.out.println("Language is python");
		}else if(lang == "C++") {
			System.out.println("Lang is C++");
		}else if(lang == "Java") {
			System.out.println("Lang is Java");
		}else {
			System.out.println(lang);
		}

	}

}
